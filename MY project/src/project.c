#define  COUNT   10
#define  FREQ_TIME    20000

void SetAngle(unsigned int duty)
{
    unsigned int time = FREQ_TIME;

    IoTGpioSetOutputVal(E53_IA1_DUOJI1_GPIO, IOT_GPIO_VALUE1);
    hi_udelay(duty);
    IoTGpioSetOutputVal(E53_IA1_DUOJI1_GPIO, IOT_GPIO_VALUE0);
    hi_udelay(time - duty);
}
